void HURDLE_set_key_fw(const uint8_t k[HURDLE_KEY_BYTES], HURDLE_CTX *lpContextOut)
{
    int i;
    int j;

    tetra_write_u32(&lpContextOut->abRoundKeys[0], tetra_read_u32(&k[0]));
    tetra_write_u32(&lpContextOut->abRoundKeys[4], tetra_read_u32(&k[4]));
    tetra_write_u32(&lpContextOut->abRoundKeys[8], tetra_read_u32(&k[8]));
    tetra_write_u32(&lpContextOut->abRoundKeys[12], tetra_read_u32(&k[12]));

    for (i = 1; i < (int)HURDLE_ROUNDS; i++) {
        const uint8_t *prev = &lpContextOut->abRoundKeys[(i - 1) * HURDLE_KEY_BYTES];
        uint8_t *cur = &lpContextOut->abRoundKeys[i * HURDLE_KEY_BYTES];
        const uint8_t rot = g_abHurdleFwRotations[i];

        for (j = 0; j < (int)HURDLE_KEY_BYTES; j++) {
            cur[j] = prev[(j + rot) & 0x0f];
        }

        tetra_write_u32(&cur[0], tetra_read_u32(&cur[0]) ^ tetra_read_u32(&g_abHurdleRoundConstant[0]));
        tetra_write_u32(&cur[4], tetra_read_u32(&cur[4]) ^ tetra_read_u32(&g_abHurdleRoundConstant[4]));
        tetra_write_u32(&cur[8], tetra_read_u32(&cur[8]) ^ tetra_read_u32(&g_abHurdleRoundConstant[8]));
        tetra_write_u32(&cur[12], tetra_read_u32(&cur[12]) ^ tetra_read_u32(&g_abHurdleRoundConstant[12]));
    }
}

void HURDLE_set_key(const uint8_t k[HURDLE_KEY_BYTES], HURDLE_CTX *lpContextOut)
{
    int i;

    for (i = 0; i < 256; i++) {
        lpContextOut->abRoundKeys[i] = k[g_abHurdleKeyIdx[i]] ^ g_abHurdleKeyXorConsts[i];
    }
}

void HURDLE_f(uint8_t abOutput[4], const uint8_t abRhs[4], const uint8_t *lpRoundKey)
{
    uint32_t dwOutputBits = 0;
    uint8_t bSboxState;

    bSboxState = g_abHurdleSbox[(abRhs[3] + lpRoundKey[15]) & 0xff];
    bSboxState = g_abHurdleSbox[((abRhs[2] + lpRoundKey[14]) ^ bSboxState) & 0xff];
    bSboxState = g_abHurdleSbox[((abRhs[1] + lpRoundKey[13]) ^ bSboxState) & 0xff];
    bSboxState = g_abHurdleSbox[((abRhs[0] + lpRoundKey[12]) ^ bSboxState) & 0xff];
    bSboxState = g_abHurdleSbox[((abRhs[3] + lpRoundKey[11]) ^ bSboxState) & 0xff];
    dwOutputBits >>= 1;
    dwOutputBits |= g_adwHurdleReorder[bSboxState & 0x0f];
    bSboxState = g_abHurdleSbox[((abRhs[1] + lpRoundKey[10]) ^ bSboxState) & 0xff];
    dwOutputBits >>= 1;
    dwOutputBits |= g_adwHurdleReorder[bSboxState & 0x0f];
    bSboxState = g_abHurdleSbox[((abRhs[2] + lpRoundKey[9]) ^ bSboxState) & 0xff];
    dwOutputBits >>= 1;
    dwOutputBits |= g_adwHurdleReorder[bSboxState & 0x0f];
    bSboxState = g_abHurdleSbox[((abRhs[0] + lpRoundKey[8]) ^ bSboxState) & 0xff];
    dwOutputBits >>= 1;
    dwOutputBits |= g_adwHurdleReorder[bSboxState & 0x0f];
    bSboxState = g_abHurdleSbox[((abRhs[1] + lpRoundKey[7]) ^ bSboxState) & 0xff];
    dwOutputBits >>= 1;
    dwOutputBits |= g_adwHurdleReorder[bSboxState & 0x0f];
    bSboxState = g_abHurdleSbox[((abRhs[3] + lpRoundKey[6]) ^ bSboxState) & 0xff];
    dwOutputBits >>= 1;
    dwOutputBits |= g_adwHurdleReorder[bSboxState & 0x0f];
    bSboxState = g_abHurdleSbox[((abRhs[0] + lpRoundKey[5]) ^ bSboxState) & 0xff];
    dwOutputBits >>= 1;
    dwOutputBits |= g_adwHurdleReorder[bSboxState & 0x0f];
    bSboxState = g_abHurdleSbox[((abRhs[2] + lpRoundKey[4]) ^ bSboxState) & 0xff];
    dwOutputBits >>= 1;
    dwOutputBits |= g_adwHurdleReorder[bSboxState & 0x0f];

    tetra_write_u32(abOutput, dwOutputBits);
}

void HURDLE_encrypt(
    uint8_t abOutput[HURDLE_BLOCK_BYTES],
    const uint8_t abInput[HURDLE_BLOCK_BYTES],
    const HURDLE_CTX *lpKey,
    uint8_t eEncryptMode)
{
    uint32_t dwLhs;
    uint32_t dwRhs;
    uint32_t dwTemp;
    int i;
    const uint8_t *lpRoundKey = (eEncryptMode == HURDLE_DECRYPT)
        ? &lpKey->abRoundKeys[(HURDLE_ROUNDS - 1) * HURDLE_KEY_BYTES]
        : lpKey->abRoundKeys;
    const int roundStep = (eEncryptMode == HURDLE_DECRYPT) ? -(int)HURDLE_KEY_BYTES : (int)HURDLE_KEY_BYTES;

    dwLhs = tetra_read_u32(abInput);
    dwRhs = tetra_read_u32(abInput + 4);

    for (i = 0; i < (int)HURDLE_ROUNDS; i++) {
        HURDLE_f((uint8_t *)&dwTemp, (uint8_t *)&dwRhs, lpRoundKey);
        dwTemp ^= dwLhs;
        dwLhs = dwRhs;
        dwRhs = dwTemp;
        lpRoundKey += roundStep;
    }

    tetra_write_u32(abOutput, dwRhs);
    tetra_write_u32(abOutput + 4, dwLhs);
}

void HURDLE_enc_cbc(
    uint8_t abCiphertext[2 * HURDLE_BLOCK_BYTES],
    const uint8_t abPlaintext[2 * HURDLE_BLOCK_BYTES],
    const uint8_t abKey[HURDLE_KEY_BYTES])
{
    uint8_t abIntermediate[HURDLE_BLOCK_BYTES];
    HURDLE_CTX stCipher;

    HURDLE_set_key(abKey, &stCipher);
    HURDLE_encrypt(abCiphertext, abPlaintext, &stCipher, HURDLE_ENCRYPT);
    tetra_write_u32(&abIntermediate[0], tetra_read_u32(abCiphertext) ^ tetra_read_u32(abPlaintext + 8));
    tetra_write_u32(&abIntermediate[4], tetra_read_u32(abCiphertext + 4) ^ tetra_read_u32(abPlaintext + 12));
    HURDLE_encrypt(&abCiphertext[8], abIntermediate, &stCipher, HURDLE_ENCRYPT);
}

void HURDLE_dec_cts(
    uint8_t abPlaintext[15],
    const uint8_t abCiphertext[15],
    const uint8_t abKey[HURDLE_KEY_BYTES])
{
    uint8_t abIntermediate[2 * HURDLE_BLOCK_BYTES];
    HURDLE_CTX stCipher;

    HURDLE_set_key(abKey, &stCipher);

    HURDLE_encrypt(&abIntermediate[8], &abCiphertext[7], &stCipher, HURDLE_DECRYPT);
    tetra_write_u32(&abIntermediate[0], tetra_read_u32(abCiphertext));
    tetra_write_u32(&abIntermediate[4], tetra_read_u32(abCiphertext + 4));
    abIntermediate[7] = abIntermediate[15];
    HURDLE_encrypt(&abIntermediate[0], &abIntermediate[0], &stCipher, HURDLE_DECRYPT);

    tetra_write_u32(&abIntermediate[8], tetra_read_u32(&abIntermediate[8]) ^ tetra_read_u32(abCiphertext));
    tetra_write_u16(&abIntermediate[12], tetra_read_u16(&abIntermediate[12]) ^ tetra_read_u16(abCiphertext + 4));
    abIntermediate[14] ^= abCiphertext[6];

    memcpy(abPlaintext, abIntermediate, 15);
}
